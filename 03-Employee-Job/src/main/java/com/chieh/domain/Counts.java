package com.chieh.domain;

public class Counts {
    private String userCount;
    private String employeeCount;
    private String jobCount;
    private String noticeCount;
    private String roleCount;
    private String fileCount;
    private String deptCount;

    public String getUserCount() {
        return userCount;
    }

    public void setUserCount(String userCount) {
        this.userCount = userCount;
    }

    public String getEmployeeCount() {
        return employeeCount;
    }

    public void setEmployeeCount(String employeeCount) {
        this.employeeCount = employeeCount;
    }

    public String getJobCount() {
        return jobCount;
    }

    public void setJobCount(String jobCount) {
        this.jobCount = jobCount;
    }

    public String getNoticeCount() {
        return noticeCount;
    }

    public void setNoticeCount(String noticeCount) {
        this.noticeCount = noticeCount;
    }

    public String getRoleCount() {
        return roleCount;
    }

    public void setRoleCount(String roleCount) {
        this.roleCount = roleCount;
    }

    public String getFileCount() {
        return fileCount;
    }

    public void setFileCount(String fileCount) {
        this.fileCount = fileCount;
    }

    public String getDeptCount() {
        return deptCount;
    }

    public void setDeptCount(String deptCount) {
        this.deptCount = deptCount;
    }

}
